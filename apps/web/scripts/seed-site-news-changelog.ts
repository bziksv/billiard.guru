async function main() {
  const force = process.argv.includes("--force");
  const titles = NEWS.map((n) => n.title);

  const existing = await prisma.siteNews.count({
    where: {
      title: { in: titles },
      publishedAt: { gte: RANGE_START, lte: RANGE_END },
    },
  });

  if (existing > 0 && !force) {
    console.log(`Уже есть ${existing} changelog-новостей. Добавьте --force для пересоздания.`);
    return;
  }

  if (force && existing > 0) {
    await prisma.siteNews.deleteMany({
      where: { title: { in: titles }, publishedAt: { gte: RANGE_START, lte: RANGE_END } },
    });
    console.log(`Удалено ${existing} старых записей.`);
  }

  const admin = await prisma.player.findFirst({
    where: { role: "SUPERADMIN", isVerified: true },
    select: { id: true },
  });

  let created = 0;
  for (const item of NEWS) {
    const publishedAt = new Date(`${item.publishedAt}T12:00:00+03:00`);
    await prisma.siteNews.create({
      data: {
        title: item.title,
        body: item.body.trim(),
        status: "APPROVED",
        authorId: admin?.id ?? null,
        publishedAt,
        createdAt: publishedAt,
      },
    });
    created++;
    console.log(`✓ ${item.publishedAt} — ${item.title}`);
  }

  console.log(`\nГотово: ${created} новостей сервиса.`);
}

main()
  .catch((err) => {
    console.error(err);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
