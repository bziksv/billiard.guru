async function main() {
  const force = process.argv.includes("--force");
  const titles = DRAFTS.map((d) => d.title);

  const existing = await prisma.siteNews.findMany({
    where: { title: { in: titles } },
    select: { id: true, title: true, status: true },
  });

  if (existing.length > 0 && !force) {
    console.log(`Уже есть ${existing.length} черновиков/новостей с такими заголовками:`);
    for (const row of existing) {
      console.log(`  • [${row.status}] ${row.title}`);
    }
    console.log("\nДобавьте --force, чтобы удалить их и создать заново.");
    return;
  }

  if (force && existing.length > 0) {
    await prisma.siteNews.deleteMany({ where: { title: { in: titles } } });
    console.log(`Удалено ${existing.length} записей.\n`);
  }

  const admin = await prisma.player.findFirst({
    where: { role: "SUPERADMIN", isVerified: true },
    select: { id: true },
  });

  let created = 0;
  for (const item of DRAFTS) {
    const publishedAt = new Date(
      item.suggestDate.includes("T") ? item.suggestDate : `${item.suggestDate}T12:00:00+03:00`,
    );
    await prisma.siteNews.create({
      data: {
        title: item.title,
        body: item.body.trim(),
        status: "UNPUBLISHED",
        publishedAt,
        authorId: admin?.id ?? null,
      },
    });
    created++;
    console.log(`✓ черновик (${publishedAt.toISOString().slice(0, 10)}) — ${item.title}`);
  }

  console.log(`\nГотово: ${created} черновиков. Публикация: /admin/site-news`);
  console.log("Даты 9–15 июня уже проставлены — при «Снова на сайте» сохранятся.");
}

main()
  .catch((err) => {
    console.error(err);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
